namespace TetrisGame
{
    /// <summary>
    /// Represents the various shapes of Tetris figures.
    /// </summary>
    public enum ShapeType
    {
        /// <summary>
        /// A square-shaped figure.
        /// </summary>
        Square,

        /// <summary>
        /// A straight line-shaped figure.
        /// </summary>
        Line,

        /// <summary>
        /// An L-shaped figure.
        /// </summary>
        L,

        /// <summary>
        /// A reverse L-shaped figure.
        /// </summary>
        J,

        /// <summary>
        /// A Z-shaped figure.
        /// </summary>
        Z,

        /// <summary>
        /// A reverse Z-shaped figure.
        /// </summary>
        S,

        /// <summary>
        /// A T-shaped figure.
        /// </summary>
        T
    }
}
